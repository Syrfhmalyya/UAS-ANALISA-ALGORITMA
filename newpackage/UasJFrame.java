    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nama = txtnama.getText();
        Integer nomor = Integer.parseInt (txtnik.getText());
        String pilih = combopilih.getSelectedItem().toString();
        analoglist.add(new analog(nama,pilih,nomor));
        dtm.setRowCount(0);
        for (int i=0; i<analoglist.size(); i++){
            Object [] objs = {analoglist.get(i).nama,analoglist.get(i).nomor,analoglist.get(i).pilih};
            dtm.addRow(objs);
        }
        clear();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this,"Delete this data","Delete",dialogButton);
        if (dialogResult == 0){
            dtm.removeRow (row);
            analoglist.remove (row);
            dtm.setRowCount(0);
            for(int i =0; i < analoglist.size();i++){
            Object [] objs = {analoglist.get(i).nama,analoglist.get(i).nomor,analoglist.get(i).pilih};
            dtm.addRow(objs);
            }clear();
            }else{
            
        }               
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String updatenama = txtnama.getText();
        Integer updatenik = Integer.parseInt(txtnik.getText());
        String updatepilih = combopilih.getSelectedItem().toString();
        analoglist.get(row).nama = updatenama;
        analoglist.get(row).nomor = updatenik;
        analoglist.get(row).pilih = updatepilih;
        dtm.setRowCount(0);
        for (int i = 0;i < analoglist.size(); i++) {
            Object [] objs ={analoglist.get(i).nama,analoglist.get(i).nomor,analoglist.get(i).pilih};
            dtm.addRow(objs);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String in =JOptionPane.showInputDialog(this,"Mencari Data");
        for (int i = 0;i < analoglist.size(); i++){
        if (analoglist.get(i).nama.equalsIgnoreCase(in)){
            JOptionPane.showMessageDialog(jButton4, "ketemu","Cari Nama",2);
            txtnama.setText(analoglist.get(i).nama);
            txtnik.setText(String.valueOf(analoglist.get(i).nomor));
            String location = analoglist.get(i).nama;
            for (int j= 0; j<(combopilih.getItemCount());j++){
                if (combopilih.getItemAt(i).equalsIgnoreCase(location)){
                    combopilih.setSelectedIndex(i);
                }
            }
            return;
            
        }
          
        }
        JOptionPane.showMessageDialog(jButton4,"Tidak Ketemu","Cari Nama",2);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        row = jTable1.getSelectedRow();
        col = jTable1.getColumnCount();
        System.out.println(row+","+col);
        txtnama.setText(dtm.getValueAt(row,0).toString());
        txtnik.setText(dtm.getValueAt(row, 1).toString());
        String location =dtm.getValueAt(row,2).toString();
        for (int i = 0; i<(combopilih.getItemCount());i++){
            if (combopilih.getItemAt(i).equalsIgnoreCase(location)){
                combopilih.setSelectedIndex(i);
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked
 private void clear(){
        txtnama.requestFocus();
        txtnama.setText("");
        txtnik.setText("");
        combopilih.setSelectedIndex(0);
 }
        
    
